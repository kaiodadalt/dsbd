"""
    *** EXERCISE 3 ***

    Qual o resultado da expressão "23 + 7 * 50" ?

    a. Como obter o resultado 1500 da expressão acima? Escreva essa instrução em Python.
"""

# Calculando o resultado da expressão original
resultado = 23 + 7 * 50

# Imprimindo o resultado da expressão original
print(f"O resultado da expressão 23 + 7 * 50 é {resultado}")

# Calculando o resultado da expressão modificada
resultado_modificado = (23 + 7) * 50

# Imprimindo o resultado da expressão modificada
print(f"O resultado da expressão '(23 + 7) * 50' é {resultado_modificado}")
